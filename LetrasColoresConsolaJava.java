public class LetrasColoresConsolaJava {

    public static void main(String[] args) {
        System.out.println("hola");
        System.out.println("\u001B[32m texto \u001B[0m");// letras verdes
        System.out.println("\u001B[42m texto \u001B[0m");// fondo verde
        System.out.println("\u001B[31m texto \u001B[0m");// letras rojas
        System.out.println("\u001B[33m texto \u001B[0m");// letras amarillas 
        System.out.println("tal");
    }
}